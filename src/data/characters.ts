type ValidationResult = {
  isValid: boolean;
  errors: string[];
};

// Optional helper: run this in tests or app startup when editing roster manually.
export function validateCharactersDataset(dataset: CharacterRecord[] = characters): ValidationResult {
  const errors: string[] = [];
  const seenIds = new Set<string>();

  dataset.forEach((character, index) => {
    const label = `${character.name || "unknown"} (index ${index})`;

    if (!character.id) errors.push(`Missing id for ${label}`);
    if (!character.name) errors.push(`Missing name for ${label}`);
    if (!character.image) errors.push(`Missing image for ${label}`);
    if (!character.gender) errors.push(`Missing gender for ${label}`);
    if (!character.species) errors.push(`Missing species for ${label}`);
    if (!character.franchiseGroup) errors.push(`Missing franchiseGroup for ${label}`);
    if (!character.signatureColor) errors.push(`Missing signatureColor for ${label}`);
    if (typeof character.releaseYear !== "number" || Number.isNaN(character.releaseYear)) {
      errors.push(`Invalid releaseYear for ${label}`);
    }
    if (typeof character.isActive !== "boolean") {
      errors.push(`isActive must be boolean for ${label}`);
    }

    if (character.id) {
      if (seenIds.has(character.id)) {
        errors.push(`Duplicate id found: "${character.id}"`);
      }
      seenIds.add(character.id);
    }
  });

  return {
    isValid: errors.length === 0,
    errors,
  };
}
